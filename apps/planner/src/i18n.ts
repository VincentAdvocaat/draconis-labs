export type Messages = (typeof messages)['nl'];

export function createTranslator(locale: Locale) {
  const copy = messages[locale];
  return {
    locale,
    t: copy,
    laneLabel: (lane: (typeof LANES)[number]) => copy.lanes[lane],
    priorityLabel: (priority: Priority) => copy.priorities[priority],
    viewLabel: (view: PlannerView) => copy.views[view],
    formatDate: (isoDate: string) =>
      new Intl.DateTimeFormat(locale === 'nl' ? 'nl-NL' : 'en-US', {
        weekday: 'long',
        day: 'numeric',
        month: 'long',
      }).format(new Date(`${isoDate}T12:00:00`)),
    formatHeadingDate: (date = new Date()) =>
      new Intl.DateTimeFormat(locale === 'nl' ? 'nl-NL' : 'en-US', {
        weekday: 'long',
        day: 'numeric',
        month: 'long',
      }).format(date),
  };
}

